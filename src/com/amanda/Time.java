package com.amanda;

public class Time { //class Time sebagai super class
    public float jam, menit, detik; // deklarasi atributnya

    //method getter dan setter jam, menit, dan detik
    public float getJam() {
        return jam;
    }

    public void setJam(float jam) {
        this.jam = jam;
    }

    public float getMenit() {
        return menit;
    }

    public void setMenit(float menit) {
        this.menit = menit;
    }

    public float getDetik() {
        return detik;
    }

    public void setDetik(float detik) {
        this.detik = detik;
    }
}
